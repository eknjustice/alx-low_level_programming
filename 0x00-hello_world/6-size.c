#include <stdio.h>

int main(void)
{
	printf("size of a char is %2d bytes\n", sizeof(char));
	printf("size of an int is %2d bytes\n", sizeof(int));
	printf("size of a long int is %2d bytes\n", sizeof(long int));
	printf("size of a long long int is %2d bytes\n", sizeof(long long int));
	printf("size of a float is %2d bytes\n", sizeof(float));
	return 0;
}
